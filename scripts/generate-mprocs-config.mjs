#!/usr/bin/env node
/**
 * Generate mprocs.yaml from launch-manifest.json + .worktree-ports.env
 * Usage: node scripts/generate-mprocs-config.mjs [--stdout]
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const MANIFEST = resolve(ROOT, "scripts/launch-manifest.json");
const PORTS_ENV = resolve(ROOT, ".worktree-ports.env");
const OUT = resolve(ROOT, "mprocs.yaml");

const PORT_ENV_MAP = {
  "vibe-web-app": "VIBE_WEB_PORT",
  "web-dashboard": "WEB_DASHBOARD_PORT",
  "agent-server": "AGENT_SERVER_PORT",
  "forge-app": "FORGE_APP_PORT",
  "forge-web": "FORGE_WEB_PORT",
  "forge-api": "FORGE_API_PORT",
  "forge-docs": "FORGE_DOCS_PORT",
  "forge-storybook": "FORGE_STORYBOOK_PORT",
};

function parsePortsEnv() {
  const ports = {};
  if (!existsSync(PORTS_ENV)) return ports;
  for (const line of readFileSync(PORTS_ENV, "utf8").split(/\r?\n/)) {
    const m = line.match(/^([A-Z_]+)=(\d+)$/);
    if (m) ports[m[1]] = Number(m[2]);
  }
  return ports;
}

function portFor(app, envPorts) {
  const key = PORT_ENV_MAP[app.id];
  if (key && envPorts[key]) return envPorts[key];
  return app.port ?? null;
}

function buildProc(app, envPorts) {
  const port = portFor(app, envPorts);
  const cwd = resolve(ROOT, app.cwd).replace(/\\/g, "/");

  if (app.id === "agent-server") {
    return {
      shell: `poetry run uvicorn src.main:app --reload --host 127.0.0.1 --port ${port}`,
      cwd,
      env: { PORT: String(port) },
    };
  }

  if (app.cwd.startsWith("next-forge/")) {
    return {
      cmd: ["npx", "bun", "run", app.devScript ?? "dev"],
      cwd,
      env: port ? { PORT: String(port) } : {},
    };
  }

  return {
    cmd: ["yarn", app.devScript ?? "dev"],
    cwd,
    env: port ? { PORT: String(port) } : {},
  };
}

function toYaml(procs) {
  const lines = [
    "# Generated by scripts/generate-mprocs-config.mjs — re-run after port slot changes",
    "# yarn agent:mprocs:generate && yarn agent:tui",
    "mouse_scroll_speed: 1",
    "procs:",
  ];

  for (const [name, proc] of Object.entries(procs)) {
    lines.push(`  ${name}:`);
    if (proc.cmd) {
      lines.push(`    cmd: [${proc.cmd.map((c) => JSON.stringify(c)).join(", ")}]`);
    }
    if (proc.shell) {
      lines.push(`    shell: ${JSON.stringify(proc.shell)}`);
    }
    lines.push(`    cwd: ${JSON.stringify(proc.cwd)}`);
    if (proc.env && Object.keys(proc.env).length > 0) {
      lines.push("    env:");
      for (const [k, v] of Object.entries(proc.env)) {
        lines.push(`      ${k}: ${JSON.stringify(v)}`);
      }
    }
    lines.push("    autostart: false");
  }

  return `${lines.join("\n")}\n`;
}

function main() {
  const manifest = JSON.parse(readFileSync(MANIFEST, "utf8"));
  const envPorts = parsePortsEnv();
  const includeIds = new Set([
    "vibe-web-app",
    "web-dashboard",
    "agent-server",
    "forge-app",
    "forge-api",
  ]);

  const procs = {};
  for (const app of manifest.apps) {
    if (!includeIds.has(app.id)) continue;
    if (!app.cwd) continue;
    const key = app.id.replace(/-/g, "_");
    procs[key] = buildProc(app, envPorts);
  }

  procs.orchestrator_status = {
    shell: "node scripts/agent-status.mjs",
    cwd: ROOT.replace(/\\/g, "/"),
    env: {},
    autostart: false,
  };

  const yaml = toYaml(procs);
  if (process.argv.includes("--stdout")) {
    process.stdout.write(yaml);
    return;
  }
  writeFileSync(OUT, yaml, "utf8");
  console.log(`wrote ${OUT}`);
}

main();
